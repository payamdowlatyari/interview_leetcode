/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 * @param {number} val
 * @return {ListNode}
 */
export class ListNode {
    /**
     * Creates a new ListNode.
     */
    constructor(val) {
        this.val = val;
        this.next = null;
    }
};